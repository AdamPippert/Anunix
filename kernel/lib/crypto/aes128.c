static const uint8_t rcon128[10] = {
	0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36,
};

/* GF(2^8) multiply by 2 */
static uint8_t xtime(uint8_t x)
{
	return (uint8_t)((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

/* GF(2^8) multiply by n (using repeated xtime) */
static uint8_t gmul(uint8_t a, uint8_t b)
{
	uint8_t p = 0;
	uint32_t i;

	for (i = 0; i < 8; i++) {
		if (b & 1) p ^= a;
		a = xtime(a);
		b >>= 1;
	}
	return p;
}

/* AES-128 key expansion: 16-byte key → 176 bytes (11 round keys) */
static void aes128_expand_key(const uint8_t key[16], uint8_t rk[176])
{
	uint32_t i;
	uint8_t temp[4];

	anx_memcpy(rk, key, 16);

	for (i = 4; i < 44; i++) {
		temp[0] = rk[(i - 1) * 4 + 0];
		temp[1] = rk[(i - 1) * 4 + 1];
		temp[2] = rk[(i - 1) * 4 + 2];
		temp[3] = rk[(i - 1) * 4 + 3];

		if (i % 4 == 0) {
			uint8_t t = temp[0];
			temp[0] = sbox[temp[1]] ^ rcon128[i / 4 - 1];
			temp[1] = sbox[temp[2]];
			temp[2] = sbox[temp[3]];
			temp[3] = sbox[t];
		}

		rk[i * 4 + 0] = rk[(i - 4) * 4 + 0] ^ temp[0];
		rk[i * 4 + 1] = rk[(i - 4) * 4 + 1] ^ temp[1];
		rk[i * 4 + 2] = rk[(i - 4) * 4 + 2] ^ temp[2];
		rk[i * 4 + 3] = rk[(i - 4) * 4 + 3] ^ temp[3];
	}
}

static void add_round_key128(uint8_t state[16], const uint8_t *rk)
{
	uint32_t i;

	for (i = 0; i < 16; i++)
		state[i] ^= rk[i];
}

/* InvSubBytes */
static void inv_sub_bytes(uint8_t state[16])
{
	uint32_t i;

	for (i = 0; i < 16; i++)
		state[i] = inv_sbox[state[i]];
}

/* InvShiftRows: shift rows right */
static void inv_shift_rows(uint8_t state[16])
{
	uint8_t t;

	/* Row 1: shift right by 1 */
	t = state[13]; state[13] = state[9]; state[9] = state[5];
	state[5] = state[1]; state[1] = t;

	/* Row 2: shift right by 2 */
	t = state[2]; state[2] = state[10]; state[10] = t;
	t = state[6]; state[6] = state[14]; state[14] = t;

	/* Row 3: shift right by 3 */
	t = state[3]; state[3] = state[7]; state[7] = state[11];
	state[11] = state[15]; state[15] = t;
}

/* InvMixColumns */
static void inv_mix_columns(uint8_t state[16])
{
	uint32_t i;
	uint8_t a, b, c, d;

	for (i = 0; i < 16; i += 4) {
		a = state[i]; b = state[i+1]; c = state[i+2]; d = state[i+3];
		state[i]   = gmul(a, 14) ^ gmul(b, 11) ^ gmul(c, 13) ^ gmul(d,  9);
		state[i+1] = gmul(a,  9) ^ gmul(b, 14) ^ gmul(c, 11) ^ gmul(d, 13);
		state[i+2] = gmul(a, 13) ^ gmul(b,  9) ^ gmul(c, 14) ^ gmul(d, 11);
		state[i+3] = gmul(a, 11) ^ gmul(b, 13) ^ gmul(c,  9) ^ gmul(d, 14);
	}
}

/* AES-128 decrypt one 16-byte block */
static void aes128_decrypt_block(const uint8_t rk[176],
				 const uint8_t in[16], uint8_t out[16])
{
	uint8_t state[16];
	int32_t round;

	anx_memcpy(state, in, 16);
	add_round_key128(state, rk + 10 * 16);

	for (round = 9; round >= 1; round--) {
		inv_shift_rows(state);
		inv_sub_bytes(state);
		add_round_key128(state, rk + round * 16);
		inv_mix_columns(state);
	}

	inv_shift_rows(state);
	inv_sub_bytes(state);
	add_round_key128(state, rk);

	anx_memcpy(out, state, 16);
}

/*
 * RFC 3394 AES-128 key unwrap.
 *
 * kek:      16-byte Key Encryption Key
 * wrapped:  (n+1)*8 bytes of wrapped key data
 * n:        number of 64-bit blocks in the plaintext key (= wrapped_len/8 - 1)
 * out:      n*8 bytes output buffer
 *
 * Returns 0 on success, -1 if integrity check fails.
 * For a 16-byte (128-bit) GTK: n=2, wrapped_len=24, out_len=16.
 */
int anx_aes128_unwrap(const uint8_t kek[16],
		      const uint8_t *wrapped, uint32_t wrapped_len,
		      uint8_t *out, uint32_t out_len)
{
	uint8_t rk[176];
	uint8_t A[8], B[16];
	uint8_t *R;
	int32_t j, i;
	uint32_t n, t;

	if (wrapped_len < 16 || (wrapped_len % 8) != 0)
		return -1;

	n = wrapped_len / 8 - 1;
	if (n * 8 != out_len)
		return -1;

	aes128_expand_key(kek, rk);

	/* Copy wrapped to working buffers: A = C[0], R[i] = C[i] */
	anx_memcpy(A, wrapped, 8);
	anx_memcpy(out, wrapped + 8, n * 8);
	R = out;

	/* Unwrap: 6 rounds, n blocks per round */
	for (j = 5; j >= 0; j--) {
		for (i = (int32_t)n; i >= 1; i--) {
			t = (uint32_t)((uint32_t)n * (uint32_t)j + (uint32_t)i);
			/* XOR t into A (big-endian, rightmost byte) */
			B[0] = A[0]; B[1] = A[1]; B[2] = A[2]; B[3] = A[3];
			B[4] = A[4]; B[5] = A[5]; B[6] = A[6];
			B[7] = A[7] ^ (uint8_t)(t & 0xff);
			if (t > 0xff) {
				B[6] ^= (uint8_t)((t >> 8) & 0xff);
				if (t > 0xffff) {
					B[5] ^= (uint8_t)((t >> 16) & 0xff);
					if (t > 0xffffff)
						B[4] ^= (uint8_t)((t >> 24) & 0xff);
				}
			}
			anx_memcpy(B + 8, R + (i - 1) * 8, 8);
			aes128_decrypt_block(rk, B, B);
			anx_memcpy(A, B, 8);
			anx_memcpy(R + (i - 1) * 8, B + 8, 8);
		}
	}

	anx_memset(rk, 0, sizeof(rk));

	/* Verify integrity: A must be the default IV */
	for (i = 0; i < 8; i++) {
		if (A[i] != 0xA6)
			return -1;
	}
	return 0;
}
