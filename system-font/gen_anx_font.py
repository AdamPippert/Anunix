def write_c_header(path: Path) -> None:
    lines: list[str] = []
    a = lines.append

    a('/*')
    a(' * anx_schoolbook_12x24.h — ANX Schoolbook system font, 12x24 pixels.')
    a(' *')
    a(' * Each glyph is 24 uint16_t rows.  Bit 11 (0x800) = leftmost pixel.')
    a(' * Covers ASCII 0x20–0x7E (95 printable characters).')
    a(' *')
    a(' * Generated by gen_anx_font.py — do not edit by hand.')
    a(' */')
    a('')
    a('#ifndef ANX_SCHOOLBOOK_12X24_H')
    a('#define ANX_SCHOOLBOOK_12X24_H')
    a('')
    a('#include <anx/types.h>')
    a('')
    a('#define ANX_FONT12_WIDTH   12')
    a('#define ANX_FONT12_HEIGHT  24')
    a('#define ANX_FONT12_FIRST   0x20')
    a('#define ANX_FONT12_LAST    0x7E')
    a('#define ANX_FONT12_COUNT   95')
    a('')
    a('/*')
    a(' * Index with (character - ANX_FONT12_FIRST).')
    a(' * Each row is a 12-bit mask; bit 11 is the leftmost pixel.')
    a(' */')
    a('static const uint16_t anx_font12[ANX_FONT12_COUNT][ANX_FONT12_HEIGHT] = {')

    for cp in range(FIRST, LAST + 1):
        rows = _G[cp]
        ch = chr(cp)
        if ch.isprintable() and ch != '/' and ch != '*':
            label = ch
        else:
            label = f'0x{cp:02X}'
        a(f'\t/* {label!r:5s} 0x{cp:02X} */ {{')
        hex_vals = ', '.join(f'0x{v:03X}' for v in rows)
        a(f'\t\t{hex_vals}')
        a('\t},')

    a('};')
    a('')
    a('#endif /* ANX_SCHOOLBOOK_12X24_H */')
    a('')

    path.write_text('\n'.join(lines), encoding='utf-8')
    print(f'  wrote {path}')

# ─────────────────────────────────────────────────────────────
# Entry point
# ─────────────────────────────────────────────────────────────

def main() -> None:
    _check()

    out = Path(sys.argv[1]) if len(sys.argv) > 1 else HERE
    out.mkdir(parents=True, exist_ok=True)

    print('ANX Schoolbook 12×24 — generating outputs ...')
    write_bdf(out / 'anx_schoolbook_12x24.bdf')
    write_ttf(out / 'anx_schoolbook_12x24.otf')
    write_c_header(out / 'anx_schoolbook_12x24.h')
    print('done.')

if __name__ == '__main__':
    main()
