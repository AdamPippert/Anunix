W           = 12      # glyph width (pixels)
H           = 24      # glyph height (pixels)
BL          = 18      # baseline row (0 = top)
UPM         = 2400    # units per em  (100 units per pixel)
PX          = 100     # units per pixel = UPM / H
ADVANCE     = W * PX  # horizontal advance = 1200
ASCENDER    = BL * PX                  # 1800
DESCENDER   = -(H - BL - 1) * PX      # -500
FIRST       = 0x20
LAST        = 0x7E

# ── Row helper ────────────────────────────────────────────────
def _row(s: str) -> int:
    """'##..#.' (12 chars) → 12-bit int, bit-11 = leftmost pixel."""
    assert len(s) == W, f"row must be {W} chars: {s!r}"
    v = 0
    for c in s:
        v = (v << 1) | (c == '#')
    return v

def glyph(*rows: str) -> list[int]:
    assert len(rows) <= H, f"too many rows: {len(rows)} > {H}"
    result = [_row(r) for r in rows]
    result += [0] * (H - len(rows))   # pad trailing blank rows
    return result

_G: dict[int, list[int]] = {}

def reg(cp: int, *rows: str) -> None:
    _G[cp] = glyph(*rows)

