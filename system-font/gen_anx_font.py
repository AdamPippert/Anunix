# Verify completeness
# ─────────────────────────────────────────────────────────────
def _check() -> None:
    missing = [f"0x{cp:02X}" for cp in range(FIRST, LAST + 1) if cp not in _G]
    if missing:
        raise RuntimeError(f"Missing glyphs: {missing}")

# ─────────────────────────────────────────────────────────────
# BDF output
# ─────────────────────────────────────────────────────────────
#
# BDF baseline convention: y_offset = -(H - BL - 1)
# so that baseline row BL maps to y=0.
# With H=24, BL=18: y_offset = -(24 - 18 - 1) = -5
Y_OFF = -(H - BL - 1)   # -5

def _glyph_to_bdf_hex(rows: list[int]) -> list[str]:
    """Each 12-bit row → 2 hex bytes (left-aligned in 16 bits)."""
    result = []
    for v in rows:
        word = v << 4          # align 12 bits into high 12 of 16
        result.append(f'{word:04X}')
    return result

def write_bdf(path: Path) -> None:
    lines: list[str] = []
    a = lines.append

    dpi = 96
    pt  = 24

    a('STARTFONT 2.1')
    a(f'FONT -{FONT_NAME}-Medium-R-Normal--{H}-{pt * 10}-{dpi}-{dpi}-C-{W * 10}-ISO10646-1')
    a(f'SIZE {pt} {dpi} {dpi}')
    a(f'FONTBOUNDINGBOX {W} {H} 0 {Y_OFF}')
    a('STARTPROPERTIES 11')
    a(f'FAMILY_NAME "{FAMILY}"')
    a(f'WEIGHT_NAME "Medium"')
    a(f'SLANT "R"')
    a(f'POINT_SIZE {pt * 10}')
    a(f'PIXEL_SIZE {H}')
    a(f'RESOLUTION_X {dpi}')
    a(f'RESOLUTION_Y {dpi}')
    a(f'SPACING "C"')
    a(f'AVERAGE_WIDTH {W * 10}')
    a(f'CHARSET_REGISTRY "ISO10646"')
    a(f'CHARSET_ENCODING "1"')
    a('ENDPROPERTIES')
    a(f'CHARS {LAST - FIRST + 1}')

    for cp in range(FIRST, LAST + 1):
        rows = _G[cp]
        char_name = chr(cp) if chr(cp).isalnum() else f'char{cp}'
        a(f'STARTCHAR {char_name}')
        a(f'ENCODING {cp}')
        a(f'SWIDTH {W * 1000 // H} 0')
        a(f'DWIDTH {W} 0')
        a(f'BBX {W} {H} 0 {Y_OFF}')
        a('BITMAP')
        for hex_row in _glyph_to_bdf_hex(rows):
            a(hex_row)
        a('ENDCHAR')

    a('ENDFONT')

    path.write_text('\n'.join(lines) + '\n', encoding='ascii')
    print(f'  wrote {path}')

# ─────────────────────────────────────────────────────────────
# TTF output via fontTools (pixel-art outline font, TTF/TrueType)
# Each on-pixel becomes a filled unit square (4-point contour).
# ─────────────────────────────────────────────────────────────

def write_ttf(path: Path) -> None:
    try:
        from fontTools.fontBuilder import FontBuilder
        from fontTools.pens.ttGlyphPen import TTGlyphPen
    except ImportError:
        print('  fonttools not available — skipping TTF output')
        return

    glyph_order = ['.notdef']
    cmap: dict[int, str] = {}
    for cp in range(FIRST, LAST + 1):
        name = f'uni{cp:04X}'
        glyph_order.append(name)
        cmap[cp] = name

    def draw_pixel_cw(pen: 'TTGlyphPen', x0: int, y0: int) -> None:
        """Draw one lit pixel as a clockwise unit square (TrueType outer contour).
        TrueType fill rule: clockwise = outer (filled), CCW = inner (hole).
        Traverse bottom-left → bottom-right → top-right → top-left."""
        x1, y1 = x0 + PX, y0 + PX
        pen.moveTo((x0, y0))
        pen.lineTo((x1, y0))
        pen.lineTo((x1, y1))
        pen.lineTo((x0, y1))
        pen.closePath()

    def draw_pixels(rows: list[int], pen: 'TTGlyphPen') -> None:
        for r, val in enumerate(rows):
            for c in range(W):
                if val & (1 << (W - 1 - c)):
                    draw_pixel_cw(pen, c * PX, (BL - r) * PX)

    metrics:  dict[str, tuple[int, int]] = {}
    glyf_tbl: dict[str, object]          = {}

    # .notdef: hollow rectangle (CW outer, CCW inner)
    pen = TTGlyphPen(None)
    for r in (2, 22):
        for c in range(W):
            draw_pixel_cw(pen, c * PX, (BL - r) * PX)
    for r in range(3, 22):
        for c in (0, W - 1):
            draw_pixel_cw(pen, c * PX, (BL - r) * PX)
    glyf_tbl['.notdef'] = pen.glyph()
    metrics['.notdef']  = (ADVANCE, 0)

    for cp in range(FIRST, LAST + 1):
        name = f'uni{cp:04X}'
        pen  = TTGlyphPen(None)
        draw_pixels(_G[cp], pen)
        glyf_tbl[name] = pen.glyph()
        metrics[name]  = (ADVANCE, 0)

    VERSION     = '1.000'
    PS_NAME     = 'ANXSchoolbook-Regular'
    UNIQUE_ID   = f'{VERSION};ANX ;{PS_NAME}'

    fb = FontBuilder(UPM, isTTF=True)
    fb.setupGlyphOrder(glyph_order)
    fb.setupCharacterMap(cmap)
    fb.setupGlyf(glyf_tbl)
    fb.setupHorizontalMetrics(metrics)
    fb.setupHorizontalHeader(ascent=ASCENDER, descent=DESCENDER)
    fb.setupNameTable({
        'familyName':   FAMILY,
        'styleName':    'Regular',
        'uniqueFontIdentifier': UNIQUE_ID,
        'fullName':     f'{FAMILY} Regular',
        'version':      f'Version {VERSION}',
        'psName':       PS_NAME,
    })
    fb.setupOS2(
        sTypoAscender=ASCENDER,
        sTypoDescender=DESCENDER,
        sTypoLineGap=0,
        usWinAscent=ASCENDER,
        usWinDescent=-DESCENDER,
        fsType=0,
        fsSelection=0x40,   # bit 6 = REGULAR
        achVendID='ANX ',
    )
    fb.setupPost(isFixedPitch=1)
    # Set lowestRecPPEM to the design pixel size (24 px at UPM=2400 → 1 px = 100u)
    fb.font['head'].lowestRecPPEM = H
    fb.font.save(str(path))
    print(f'  wrote {path}')

