/*
 * test_crypto.c — Tests for crypto primitives (SHA-256, Ed25519, etc.)
 */

#include <anx/types.h>
#include <anx/crypto.h>
#include <anx/string.h>

int test_crypto(void)
{
	/* --- Test 1: SHA-256 empty string --- */
	{
		uint8_t hash[32];
		/* Expected: e3b0c44298fc1c149afbf4c8996fb92427ae41e4649b934ca495991b7852b855 */
		const uint8_t expected[32] = {
			0xe3, 0xb0, 0xc4, 0x42, 0x98, 0xfc, 0x1c, 0x14,
			0x9a, 0xfb, 0xf4, 0xc8, 0x99, 0x6f, 0xb9, 0x24,
			0x27, 0xae, 0x41, 0xe4, 0x64, 0x9b, 0x93, 0x4c,
			0xa4, 0x95, 0x99, 0x1b, 0x78, 0x52, 0xb8, 0x55
		};

		anx_sha256("", 0, hash);
		if (anx_memcmp(hash, expected, 32) != 0)
			return -1;
	}

	/* --- Test 2: SHA-256 "abc" --- */
	{
		uint8_t hash[32];
		const uint8_t expected[32] = {
			0xba, 0x78, 0x16, 0xbf, 0x8f, 0x01, 0xcf, 0xea,
			0x41, 0x41, 0x40, 0xde, 0x5d, 0xae, 0x22, 0x23,
			0xb0, 0x03, 0x61, 0xa3, 0x96, 0x17, 0x7a, 0x9c,
			0xb4, 0x10, 0xff, 0x61, 0xf2, 0x00, 0x15, 0xad
		};

		anx_sha256("abc", 3, hash);
		if (anx_memcmp(hash, expected, 32) != 0)
			return -2;
	}

	/* --- Test 3: Ed25519 sign/verify round-trip --- */
	{
		uint8_t seed[32];
		uint8_t pub[32];
		uint8_t priv[64];
		uint8_t sig[64];
		const char *msg = "Hello, Anunix SSH!";
		uint32_t msg_len = (uint32_t)anx_strlen(msg);
		uint32_t i;

		/* Deterministic seed for reproducibility */
		for (i = 0; i < 32; i++)
			seed[i] = (uint8_t)i;

		anx_ed25519_keypair(pub, priv, seed);
		anx_ed25519_sign(sig, msg, msg_len, priv);

		if (anx_ed25519_verify(sig, msg, msg_len, pub) != 0)
			return -3;

		/* Tamper with message — verify must fail */
		if (anx_ed25519_verify(sig, "tampered msg!", 13, pub) == 0)
			return -4;

		/* Tamper with signature — verify must fail */
		sig[0] ^= 1;
		if (anx_ed25519_verify(sig, msg, msg_len, pub) == 0)
			return -5;
	}

	/* --- Test 4: Ed25519 RFC 8032 test vector 1 --- */
	{
		/* Test 1 from RFC 8032 section 7.1 */
		const uint8_t seed[32] = {
			0x9d, 0x61, 0xb1, 0x9d, 0xef, 0xfd, 0x5a, 0x60,
			0xba, 0x84, 0x4a, 0xf4, 0x92, 0xec, 0x2c, 0xc4,
			0x44, 0x49, 0xc5, 0x69, 0x7b, 0x32, 0x69, 0x19,
			0x70, 0x3b, 0xac, 0x03, 0x1c, 0xae, 0x7f, 0x60
		};
		const uint8_t expected_pub[32] = {
			0xd7, 0x5a, 0x98, 0x01, 0x82, 0xb1, 0x0a, 0xb7,
			0xd5, 0x4b, 0xfe, 0xd3, 0xc9, 0x64, 0x07, 0x3a,
			0x0e, 0xe1, 0x72, 0xf3, 0xda, 0xa6, 0x23, 0x25,
			0xaf, 0x02, 0x1a, 0x68, 0xf7, 0x07, 0x51, 0x1a
		};
		const uint8_t expected_sig[64] = {
			0xe5, 0x56, 0x43, 0x00, 0xc3, 0x60, 0xac, 0x72,
			0x90, 0x86, 0xe2, 0xcc, 0x80, 0x6e, 0x82, 0x8a,
			0x84, 0x87, 0x7f, 0x1e, 0xb8, 0xe5, 0xd9, 0x74,
			0xd8, 0x73, 0xe0, 0x65, 0x22, 0x49, 0x01, 0x55,
			0x5f, 0xb8, 0x82, 0x15, 0x90, 0xa3, 0x3b, 0xac,
			0xc6, 0x1e, 0x39, 0x70, 0x1c, 0xf9, 0xb4, 0x6b,
			0xd2, 0x5b, 0xf5, 0xf0, 0x59, 0x5b, 0xbe, 0x24,
			0x65, 0x51, 0x41, 0x43, 0x8e, 0x7a, 0x10, 0x0b
		};
		uint8_t pub[32];
		uint8_t priv[64];
		uint8_t sig[64];

		anx_ed25519_keypair(pub, priv, seed);
		if (anx_memcmp(pub, expected_pub, 32) != 0)
			return -6;

		anx_ed25519_sign(sig, "", 0, priv);
		if (anx_memcmp(sig, expected_sig, 64) != 0)
			return -7;

		if (anx_ed25519_verify(sig, "", 0, pub) != 0)
			return -8;
	}

	/* --- Test 5: Curve25519 RFC 7748 test vector --- */
	{
		/* Section 6.1 of RFC 7748 */
		const uint8_t alice_priv[32] = {
			0x77, 0x07, 0x6d, 0x0a, 0x73, 0x18, 0xa5, 0x7d,
			0x3c, 0x16, 0xc1, 0x72, 0x51, 0xb2, 0x66, 0x45,
			0xdf, 0x4c, 0x2f, 0x87, 0xeb, 0xc0, 0x99, 0x2a,
			0xb1, 0x77, 0xfb, 0xa5, 0x1d, 0xb9, 0x2c, 0x2a
		};
		const uint8_t alice_pub_expected[32] = {
			0x85, 0x20, 0xf0, 0x09, 0x89, 0x30, 0xa7, 0x54,
			0x74, 0x8b, 0x7d, 0xdc, 0xb4, 0x3e, 0xf7, 0x5a,
			0x0d, 0xbf, 0x3a, 0x0d, 0x26, 0x38, 0x1a, 0xf4,
			0xeb, 0xa4, 0xa9, 0x8e, 0xaa, 0x9b, 0x4e, 0x6a
		};
		uint8_t alice_pub[32];

		anx_curve25519_base(alice_pub, alice_priv);
		if (anx_memcmp(alice_pub, alice_pub_expected, 32) != 0)
			return -9;
	}

	return 0;
}
